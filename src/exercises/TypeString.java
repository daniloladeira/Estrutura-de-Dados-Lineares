package exercises;
import java.util.Arrays;

public class TypeString {

    public static void main(String[] args) {
        System.out.println("Dark Souls");
        System.out.println("Dark Souls".toLowerCase());
        System.out.println("Dark Souls".toUpperCase());
        System.out.println(Arrays.toString("Dark Souls".getBytes()));
        System.out.println("Dark Souls".length());
        System.out.println("Dark Souls".concat(" 3"));
        System.out.println("Dark Souls".startsWith("D"));
        System.out.println("Dark Souls".contains("a"));
        System.out.println("Dark Souls".contains("a"));
        System.out.println("Dark Souls".indexOf('u'));
    }
}
